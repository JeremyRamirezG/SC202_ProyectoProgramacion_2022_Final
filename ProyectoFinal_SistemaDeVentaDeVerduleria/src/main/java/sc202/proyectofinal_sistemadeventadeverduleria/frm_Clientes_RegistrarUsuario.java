    private static boolean isNumeric(String cadena) {
        try {
            Integer.parseInt(cadena);
            return true;
        } catch (NumberFormatException nfe) {
            return false;
        }
    }
    private void limpiaCajasDeArchivos() {
        txtNombre.setText("");
        txtEdad.setText("");
        txtCedula.setText("");
        txtNumero.setText("");
        txtDireccion.setText("");
        txtMetodoPago.setText("");
        txtCorreo.setText("");
        txtPassword.setText("");
    }
