    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombre.getText();
        String edad = txtEdad.getText();
        String cedula = txtCedula.getText();
        String telefono = txtNumero.getText();
        String direccionClientes = txtDireccion.getText();
        String metodoPagoClientes = txtMetodoPago.getText();
        String usuario = txtCorreo.getText();
        String contraseña = String.valueOf(txtPassword.getPassword());
        
        Cliente objCliente = new Cliente();
        ManejoPersonas objManejoPersonas = new ManejoPersonas();

        if ((nombre.length() >= 1) && (edad.length() >= 1) && (cedula.length() >= 1) && (telefono.length() >= 1) && (direccionClientes.length() >= 1) && (metodoPagoClientes.length() >= 1) && (usuario.contains("@")) && (contraseña.length() >= 5)) {
            if (isNumeric(edad)) {
                objManejoPersonas.setContraseñaSesion(contraseña);
                objCliente.setIdCliente("CLI-AR");
                objCliente.setCorreoElectronicoPersona(usuario);
                objCliente.setNombrePersona(nombre);
                objCliente.setEdadPersona(Integer.parseInt(edad));
                objCliente.setCedulaPersona(cedula);
                objCliente.setNumeroTelefonicoPersona(telefono);

                objCliente.setDireccionCliente(direccionClientes);
                objCliente.setMetodoPagoCliente(metodoPagoClientes);

                objManejoPersonas.agregarCliente(objCliente);
                limpiaCajasDeArchivos();
            } else {
                JOptionPane.showMessageDialog(null, "Ingrese los datos del tipo requerido");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Ingrese todos los datos requeridos, recuerde que la contraseña debe ser de almos 5 dígitos");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

